public class VirtualPet {

    private String name = "Fido";
    private int hungerLevel = 100;
    private int thirstLevel = 100;
    private int boredomeLevel = 100;
    private int wellnessLevel = 100;
    private int sleepLevel = 100;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getHungerLevel() {
        return hungerLevel;
    }

    public void setHungerLevel(int hungerLevel) {
        this.hungerLevel = hungerLevel;
    }

    public int getBoredomeLevel() {
        return boredomeLevel;
    }

    public void setBoredomeLevel(int boredomeLevel) {
        this.boredomeLevel = boredomeLevel;
    }

    public int getThirstLevel() {
        return thirstLevel;
    }

    public void setThirstLevel(int thirstLevel) {
        this.thirstLevel = thirstLevel;
    }

    public int getWellnessLevel() {
        return wellnessLevel;
    }

    public void setWellnessLevel(int wellnessLevel) {
        this.wellnessLevel = wellnessLevel;
    }

    public int getSleepLevel() {
        return sleepLevel;
    }

    public void setSleepLevel(int sleepLevel) {
        this.sleepLevel = sleepLevel;
    }





}

